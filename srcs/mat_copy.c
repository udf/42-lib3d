#include "lib3d.h"

void	mat_copy(t_mat dest, const t_mat src)
{
	size_t i;
	size_t j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			dest[i][j] = src[i][j];
			j++;
		}
		i++;
	}
}
